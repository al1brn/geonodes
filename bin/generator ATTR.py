class Arguments(list):
    
    def add(self, arg):
        if arg.is_self or arg.is_cls:
            self.insert(0, arg)
            
        elif arg.is_multi:
            if len(self) == 0:
                self.append(arg)
            elif self[0].is_self:
                self.insert(1, arg)
            else:
                self.insert(0, arg)
                
        else:
            self.append(arg)
    
    @property
    def scall_demo(self):
        s = ""
        for arg in self:
            sh = arg.scall_demo
            if sh != "":
                s += f", {sh}"
        if s != "":
            return s[2:]
        else:
            return s
        
    @property
    def sheader(self):
        s = ""
        for arg in self:
            sh = arg.sheader
            if sh != "":
                s += f", {sh}"
        if s != "":
            return s[2:]
        else:
            return s
        
    @property
    def scall(self):
        s = ""
        for arg in self:
            if arg.is_cls:
                continue
            
            if arg.is_multi:
                sc = arg.scall
                if sc != "":
                    s += f", {sc}"
            
        for arg in self:
            if not arg.is_multi:
                sc = arg.scall
                if sc != "":
                    s += f", {sc}"

        if s != "":
            return s[2:]
        else:
            return s

    # ---------------------------------------------------------------------------
    # Check the order of the sockets arguments
    
    def check_order(self, bl_idname):

        order = INPUT_SOCKETS_ORDER.get(bl_idname)
        if order is None:
            return
        reorder = [None] * len(order)
        for arg in self:
            if not arg.is_socket:
                continue
            
            name = arg.wsocket.bsocket.name
            if name in order:
                reorder[order[name]] = arg
        
        for arg in reorder:
            if arg is not None:
                self.remove(arg)
            
        for arg in reversed(reorder):
            if arg is not None:
                self.insert(0, arg)

    # ---------------------------------------------------------------------------
    # Generate the comments in a node call
    
    def documentation(self):
        
        sections = {"Sockets": [], "Parameters": [], "Fixed parameters": []}
        
        for arg in self:
            
            section = None
            if arg.arg_type == 'OTHER':
                if arg.header_str == "":
                    s = arg.call_str
                    section = sections["Fixed parameters"]
                else:
                    s = arg.header_str
                    section = sections[ "Parameters"]
                    
                if s == "":
                    continue
                
                section.append(s.replace("=", ":"))

            else:
                scomment = arg.scomment
                if scomment == "":
                    continue
            
                if arg.is_socket:
                    sections["Sockets"].append(scomment)
                elif arg.is_param:
                    if arg.is_fixed:
                        sections["Fixed parameters"].append(scomment)
                    else:
                        sections["Parameters"].append(scomment)
                        
        text = ""
        for section, lst in sections.items():
            if lst:
                lst.insert(0, f"## {section}")
                text += "\n    - ".join(lst)
                
        return text

